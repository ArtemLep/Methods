package com.class5;

public class DoWhileLoop {

	public static void main(String[] args) {
		int a = 10;
		do {// executes first and then checks condition
			//print once
			System.out.println(a);
			a++;
		} while (a <= 19);
		
		
//------------------------------------------WHILE LOOP

		int b = 10;

		while (b < 15) {// checks condition and executes
			System.out.println("Bye");
			b++;
		}

	}

}
