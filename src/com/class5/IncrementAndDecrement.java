package com.class5;

public class IncrementAndDecrement {

	public static void main(String[] args) {

		// post increment a++ ;

		int a = 299;
		int b = a++;

		System.out.println(a);// 300
		System.out.println(b);// 299 ---->b=a its mean 299

		// & post decrement x--;

		int z = 10;
		int c = z--;

		System.out.println(z);// 9
		System.out.println(c);// 10 c=z --> its mean 10

	}

}
